        <h2>Educational Offerings</h2>
        <p>Lakeshore State Park offers educational programs for teachers, community organizations, private events and the general public. The programs emphasize sustainability and stewardship of our natural and freshwater resources. These experiences are geared toward learners of all ages, and include walks, tours, lectures, hands-on outdoor activities and laboratory exercises.</p>

        <h3>School Groups and Community Organizations</h3>
        <p>Guest speaking, field trips and labs can be tailored to fit your current content needs and meet many of the Next Generation Science Standards (the curricular framework for the state of Wisconsin). For schools and groups with limited budgets, fee waiver applications are available to ensure access to all learners. To schedule a field trip or talk, request a copy of our Educational Programs Brochure, or for more information, please contact our Park Naturalists at 414-274-4282.</p>

